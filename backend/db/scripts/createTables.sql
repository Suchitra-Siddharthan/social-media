-- 1. User Table
CREATE TABLE Users (
    UserID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Username VARCHAR2(50) UNIQUE NOT NULL,
    Email VARCHAR2(100) UNIQUE NOT NULL,
    Password VARCHAR2(100) NOT NULL,
    DateJoined DATE NOT NULL,
    Bio CLOB,
    Location VARCHAR2(100)
);

-- 2. UserProfile Table (1-to-1 with User)
CREATE TABLE UserProfile (
    UserID NUMBER PRIMARY KEY,
    ProfilePictureURL VARCHAR2(255),
    WebsiteURL VARCHAR2(255),
    DateOfBirth DATE,
    IsPremium NUMBER(1) DEFAULT 0,
    CONSTRAINT fk_userprofile_user FOREIGN KEY (UserID) REFERENCES Users(UserID) ON DELETE CASCADE
);

-- 3. Post Table
CREATE TABLE Post (
    PostID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    UserID NUMBER NOT NULL,
    Content CLOB NOT NULL,
    PostDate TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    LikesCount NUMBER DEFAULT 0,
    CommentsCount NUMBER DEFAULT 0,
    SharesCount NUMBER DEFAULT 0,
    CONSTRAINT fk_post_user FOREIGN KEY (UserID) REFERENCES Users(UserID) ON DELETE CASCADE
);

-- 4. Like Table
CREATE TABLE PostLike (
    LikeID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PostID NUMBER,
    UserID NUMBER,
    LikeDate TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_like_post FOREIGN KEY (PostID) REFERENCES Post(PostID) ON DELETE CASCADE,
    CONSTRAINT fk_like_user FOREIGN KEY (UserID) REFERENCES Users(UserID) ON DELETE CASCADE
);

-- 5. Comment Table
CREATE TABLE Comment (
    CommentID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PostID NUMBER,
    UserID NUMBER,
    CommentText CLOB NOT NULL,
    CommentDate TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_comment_post FOREIGN KEY (PostID) REFERENCES Post(PostID) ON DELETE CASCADE,
    CONSTRAINT fk_comment_user FOREIGN KEY (UserID) REFERENCES Users(UserID) ON DELETE CASCADE
);

-- 6. Hashtag Table
CREATE TABLE Hashtag (
    HashtagID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    HashtagText VARCHAR2(100) UNIQUE NOT NULL
);

-- 7. PostHashtag Table (Many-to-Many Join Table)
CREATE TABLE PostHashtag (
    PostID NUMBER,
    HashtagID NUMBER,
    PRIMARY KEY (PostID, HashtagID),
    CONSTRAINT fk_posthashtag_post FOREIGN KEY (PostID) REFERENCES Post(PostID) ON DELETE CASCADE,
    CONSTRAINT fk_posthashtag_hashtag FOREIGN KEY (HashtagID) REFERENCES Hashtag(HashtagID) ON DELETE CASCADE
);

-- 8. Notification Table
CREATE TABLE Notification (
    NotificationID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    UserID NUMBER,
    NotificationText CLOB NOT NULL,
    NotificationDate TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_notification_user FOREIGN KEY (UserID) REFERENCES Users(UserID) ON DELETE CASCADE
);
